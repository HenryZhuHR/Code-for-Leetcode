#include <iostream>
#include <string>
#include <vector>
#include <stack>
#include <queue>
#include <algorithm>
#include <unordered_set>
using namespace std;

struct TreeNode
{
    int       val;
    TreeNode* left;
    TreeNode* right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode* left, TreeNode* right) : val(x), left(left), right(right) {}
};

// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution
{
  public:
    /**
     * 迭代遍历
     */
    vector<int> inorderTraversal_Iteration(TreeNode* root)
    {
        stack<TreeNode*> st;
        vector<int>      res;
        while (!st.empty() || root)
        {
            while (root)
            {
                st.emplace(root);
                root = root->left;
            }
            root=st.top();st.pop();
            res.emplace_back(root->val);
            root=root->right;
        }
        return res;
    }
    vector<int> inorderTraversal(TreeNode* root)
    {
        return inorderTraversal_Iteration(root);
        return inorderTraversal_Iteration(root);
    }
};
